    private void fillThongTin() {
        // Thêm kiểu dáng vào trong combo box 
        for (KieuDang kd : serviceKieuDang.getAll()) {
            cbo_KieuDang.addItem(kd.getTenKieuDang());
        }
        // add size 
        for (Size sz : serviceSize.getAll()) {
            cbo_size.addItem(String.valueOf(sz.getSize()));
        }
        // add Thương hiệu 
        for (ThuongHieu th : serviceThuongHieu.getAll()) {
            cbo_thuongHieu.addItem(th.getTenThuongHieu());
        }
        // add Nhà sản xuất 
        for (NhaSanXuat nsx : serviceNhaSanXuat.getAll()) {
            cbo_NSX.addItem(nsx.getTenNSX());
        }
        // add màu sắc 
        for (MauSac ms : serviceMauSac.getAll()) {
            cbo_mauSac.addItem(ms.getTenMau());
        }
        // add chức vụ
        for (ChucVuNhanVien chucVuNhanVien : serviceChucVu.getAllChucVu()) {
            cbo_ChucVuNhanVien.addItem(chucVuNhanVien.getTenChucVuNhanVien());
        }
        // add ca làm 
        for (CaLamNhanVien clnv : serviceCaLamNhanVien.getAllCaLamNV()) {
            cbo_CaLamNhanVien.addItem(clnv.getTenCaLam());
        }
    }
