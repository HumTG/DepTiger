    void fillTableSanPham(List<SanPham> list) {
        DefaultTableModel mol = new DefaultTableModel();
        mol = (DefaultTableModel) tbl_TenSP.getModel();
        mol.setRowCount(0);
        for (SanPham x : list) {
            mol.addRow(new Object[]{
                x.getTenSanPham(), x.getIDThuongHieu(), x.getIDKieuDang(), x.getChatLieu()
            });
        }
    }

