    private void timKiemNhanVien() {
        List<NhanVien> listSearchNV = new ArrayList<>();
        String sql = "SELECT IDNhanVien ,"
                + "[HoTen]\n"
                + "      ,ChucVu.TenChucVu\n"
                + "	  ,ChucVu.Luong\n"
                + "      ,CaLam.TenCaLam\n"
                + "	  ,CaLam.ThoiGianBatDau\n"
                + "	  ,CaLam.ThoiGianKetThuc\n"
                + "      ,[GioiTinh]\n"
                + "      ,[NgaySinh]\n"
                + "      ,[DiaChi]\n"
                + "      ,[Email]\n"
                + "      ,NhanVien.TrangThai\n"
                + "      ,NhanVien.AnSP\n"
                + "  FROM [dbo].[NhanVien] \n"
                + "	join CaLam on NhanVien.IDCaLam = CaLam.IDCaLam\n"
                + "	join ChucVu on NhanVien.IDChucVu = ChucVu.IDChucVu\n"
                + "	where NhanVien.AnSP = 0 and HoTen like N'%" + txt_TimKiemNhanVien.getText() + "%'";
        try (Connection con = DBconnect.getConnection(); PreparedStatement ps = con.prepareCall(sql)) {
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                NhanVien nvs = new NhanVien(rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7), rs.getInt(8), rs.getString(9), rs.getString(10), rs.getString(11), rs.getString(12));
                listSearchNV.add(nvs);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        this.fillTableNhanVien(listSearchNV);
    }
