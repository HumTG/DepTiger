    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
        // TODO add your handling code here:
        try {
            double TienNhan;
            double TongTien = Double.valueOf(txt_TongTienTT.getText());
            if (rdoTienMat.isSelected()) {
                try {
                    TienNhan = Double.valueOf(MSGHeper.promot(this, "Nhập Vào Số Tiền Mặt Đã Nhận"));
                    double TienTra = TienNhan - TongTien;
                    if (TienTra >= 0) {
                        MSGHeper.alert(this, "Cần Trả Lại Khách : " + TienTra + " VND");
                        HoaDon hd = new HoaDon();
                        hd.setTrangThai("Đã Thanh Toán");
                        hd.setTongTien(TongTien);
                        servicehd.updateEntity(hd, IDHd);
                        setdataTBLHD(servicehd.getALL());
                        MSGHeper.alert(this, "Thanh Toán Thành Công");
                        resetTT();
                    } else {
                        MSGHeper.alert(this, "Khách Trả Thiếu : " + TienTra + " VND");
                    }
                } catch (Exception e) {
                    MSGHeper.alert(this, "Vui Lòng Nhập Đúng");
                }
            } else {
                MSGHeper.alert(this, "Vui Lòng Đợi 1 Chút Để Hệ Thống Kiểm Tra");
                HoaDon hd = new HoaDon();
                hd.setTrangThai("Đã Thanh Toán");
                hd.setTongTien(TongTien);
                servicehd.updateEntity(hd, IDHd);
                setdataTBLHD(servicehd.getALL());
                MSGHeper.alert(this, "Thanh Toán Thành Công");
                resetTT();
            }
        } catch (Exception e) {
            MSGHeper.alert(this, "Thanh Toán Thất Bại");
        }
    }//GEN-LAST:event_btnThanhToanActionPerformed

