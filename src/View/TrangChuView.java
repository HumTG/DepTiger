    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        // TODO add your handling code here:
        try {
            VaLiDate.CheckEmpty(txtNhapSL.getText());
            int soLuong = Integer.valueOf(txtNhapSL.getText());
            if (soLuong <= 0) {
                MSGHeper.alert(this, "Kiểm Tra Lại Thông Tin Số Lượng");
            } else {
                HoaDonChiTiet hdct = new HoaDonChiTiet();
                hdct.setSoLuong(soLuong);
                if (IDHDCT > 0) {
                    HoaDonChiTiet checkhdct = servicehdct.getbyID(IDHDCT);
                    int SLcu = checkhdct.getSoLuong() - soLuong;
//                    if (SLcu > 0) {
                    int IDSPCT = checkhdct.getIDSP();
                    SanPham slSP = servicehdct.getbyIDSP(IDSPCT);
                    int sLTonKho = slSP.getSoLuongTonKho() + SLcu;
                    if (sLTonKho >= 0) {
                        servicehdct.updateSLL(hdct, IDHDCT);
                        servicehdct.setSLSP(sLTonKho, IDSPCT);
                        MSGHeper.alert(this, "Sửa Lại Số Lượng Thành Công");
                        setdataTBLHDCT(servicehdct.getALLbyIDHD(IDHd));
                        this.fillTableSanPhamHoaDon(service.getAll(txt_TimKiem.getText()));
                        txtNhapSL.setText("");
                        IDHDCT = 0;
                    } else {
                        MSGHeper.alert(this, "Số Lượng Còn Lại Không Đủ");
                    }
//                    }
                } else {
                    MSGHeper.alert(this, "Chọn Dòng Muốn Thay Đổi Số Lượng");
                }
            }
        } catch (Exception e) {
            MSGHeper.alert(this, "Kiểm Tra Lại Thông Tin Số Lượng");
            throw new RuntimeException(e);
        }
    }//GEN-LAST:event_jButton20ActionPerformed

